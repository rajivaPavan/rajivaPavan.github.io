<template>
  <div id="preloader" v-if="showLoader"></div>
</template>
<script>
export default {
  name: 'AppPreloader',
  data() {
    return {
      show: true
    }
  },
  computed: {
    showLoader() {
      return this.show;
    }
  },
  mounted() {
    window.addEventListener('load', () => {
      this.show = false;
    });
  }
}
</script>
